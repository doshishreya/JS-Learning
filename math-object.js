// Math property 

console.log(Math.PI);       // Output: 3.141592653589793
console.log(Math.E);        // Output: 2.718281828459045 (Euler's number)
console.log(Math.SQRT2);    // Output: 1.4142135623730951 (Square root of 2)

// math method

// Rounding Numbers

console.log(Math.round(4.7));   // Output: 5
console.log(Math.round(4.3));   // Output: 4

// Largest and Smallest Number

console.log(Math.max(10, 20, 5, 40)); // Output: 40
console.log(Math.min(10, 20, 5, 40)); // Output: 5

// Random Number

console.log(Math.random()); // Output: Random number between 0 and 1

// Square Root and Power

console.log(Math.sqrt(25));  // Output: 5
console.log(Math.pow(2, 3)); // Output: 8 (2^3)


