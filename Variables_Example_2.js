// Variables_Example.js - User Greeting
let userName = "Alice";
let userAge = 25;
let userLocation = "New York";

// Create a personalized greeting
let greeting = `Hello, ${userName}! You are ${userAge} years old and live in ${userLocation}.`;

// Display the greeting
console.log(greeting); // Output: Hello, Alice! You are 25 years old and live in New York.

// Update user information
userAge = 26;
greeting = `Hello, ${userName}! You are now ${userAge} years old and live in ${userLocation}.`;

// Display updated greeting
console.log(greeting); // Output: Hello, Alice! You are now 26 years old and live in New York.