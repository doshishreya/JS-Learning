// DataTypes_Example.js - Primitive Data Types
let name = "John"; // String
let age = 30; // Number
let isStudent = false; // Boolean
let job = null; // Null (intentional absence of value)
let salary; // Undefined (variable declared but not assigned)

console.log(`Name: ${name}, Type: ${typeof name}`); // Output: Name: John, Type: string
console.log(`Age: ${age}, Type: ${typeof age}`); // Output: Age: 30, Type: number
console.log(`Is Student: ${isStudent}, Type: ${typeof isStudent}`); // Output: Is Student: false, Type: boolean
console.log(`Job: ${job}, Type: ${typeof job}`); // Output: Job: null, Type: object
console.log(`Salary: ${salary}, Type: ${typeof salary}`); // Output: Salary: undefined, Type: undefined