// Operators_Example.js - Comparison Operators
let x = 5;
let y = "5";
let z = 10;

console.log(`x == y: ${x == y}`); // Output: x == y: true (loose equality, checks value only)
console.log(`x === y: ${x === y}`); // Output: x === y: false (strict equality, checks value and type)
console.log(`x != z: ${x != z}`); // Output: x != z: true (loose inequality)
console.log(`x !== y: ${x !== y}`); // Output: x !== y: true (strict inequality)
console.log(`x > z: ${x > z}`); // Output: x > z: false
console.log(`x < z: ${x < z}`); // Output: x < z: true
console.log(`x >= 5: ${x >= 5}`); // Output: x >= 5: true
console.log(`z <= 10: ${z <= 10}`); // Output: z <= 10: true