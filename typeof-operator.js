// Different data types
let name = "Alice";       // String
let age = 25;             // Number
let isStudent = true;     // Boolean
let fruits = ["Apple", "Banana"]; // Object (Array)
let person = { name: "Bob", age: 30 }; // Object
let undefinedVar;         // Undefined
let emptyValue = null;    // Object (Special case)

// Checking types using typeof
console.log(typeof name);        // Output: string
console.log(typeof age);         // Output: number
console.log(typeof isStudent);   // Output: boolean
console.log(typeof fruits);      // Output: object
console.log(typeof person);      // Output: object
console.log(typeof undefinedVar);// Output: undefined
console.log(typeof emptyValue);  // Output: object (JavaScript quirk)
